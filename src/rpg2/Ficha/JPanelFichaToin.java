    private void jButtonImagemClasse4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImagemClasse4ActionPerformed
        // foto =  new ImageIcon(ClassLoader.getSystemResource("rpg2/Imagens/Classes/FN.png"));
        JFrame frame = new JFrame();
        String[] options = {"Modificar imagem"};
        int opcao = JOptionPane.showOptionDialog(frame.getContentPane(), null, "Imagem do jogador", 0, JOptionPane.PLAIN_MESSAGE, foto, options, null);
        // -1 = cancela; 0 - creditar; 1 - debitar;
        if (opcao == -1) {
            return;
        } else {
            JFileChooser fc = new JFileChooser();
            fc.setCurrentDirectory(null);
            fc.setAcceptAllFileFilterUsed(false);
            fc.addChoosableFileFilter(new FileNameExtensionFilter("Todas as imagens", "jpg", "jpeg", "png"));
            fc.addChoosableFileFilter(new FileNameExtensionFilter("Imagens em JPEG", "jpg", "jpeg"));
            fc.addChoosableFileFilter(new FileNameExtensionFilter("Imagens em PNG", "png"));
            fc.setMultiSelectionEnabled(false);
            fc.setDialogTitle("Selecione uma imagem no máximo 800x600");
            int file = fc.showOpenDialog(this);

            foto = Imagens.setImageFile(file, fc);
            jButtonImagemClasse4.doClick();
        }

    }//GEN-LAST:event_jButtonImagemClasse4ActionPerformed

