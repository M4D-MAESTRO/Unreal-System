    private void jButtonGoldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGoldActionPerformed
        JFrame frame = new JFrame();
        String[] options = {"Creditar", "Debitar"};
        int opcao = JOptionPane.showOptionDialog(frame.getContentPane(), "O que desejas fazer?", "Creditar/Debitar", 0, JOptionPane.WARNING_MESSAGE, null, options, null);
        // -1 = cancela; 0 - creditar; 1 - debitar;
        if (opcao == -1) {
            return;
        }

        double valor, atual = Double.parseDouble(jTextFieldGold.getText());
        if (opcao == 0) {
            valor = Double.parseDouble(JOptionPane.showInputDialog(null, "Insira a quantidade de ouro a ser creditado: ", "Insira o valor", JOptionPane.QUESTION_MESSAGE));
            atual += valor;
        } else {
            valor = Double.parseDouble(JOptionPane.showInputDialog(null, "Insira a quantidade de ouro a ser debitado: ", "Insira o valor", JOptionPane.QUESTION_MESSAGE));
            atual -= valor;
        }

        jTextFieldGold.setText(String.valueOf(atual));
    }//GEN-LAST:event_jButtonGoldActionPerformed

