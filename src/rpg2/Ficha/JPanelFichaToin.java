    private void jButtonConfirmarUPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarUPActionPerformed
        int pontos = Integer.parseInt(jTextFieldPontosDisponiveis4.getText());

        if (pontos > 0) {
            if (jRadioButtonPV.isSelected()) {
                Ficha.uparEspecial(jTextFieldPV, jTextFieldPontosDisponiveis4);
                return;
            }

            if (jRadioButtonPM.isSelected()) {
                Ficha.uparEspecial(jTextFieldPM, jTextFieldPontosDisponiveis4);
                return;
            }

            if (jRadioButtonFOR.isSelected()) {
                Ficha.upar(jTextFieldFOR, jTextFieldPontosDisponiveis4);
                Status.setTotais(jTextFieldATKF, jTextFieldFOR);
                return;
            }

            if (jRadioButtonDES.isSelected()) {
                Ficha.upar(jTextFieldDES, jTextFieldPontosDisponiveis4);
                Status.setTotais(jTextFieldATKD, jTextFieldDES);
                return;
            }

            if (jRadioButtonINT.isSelected()) {
                Ficha.upar(jTextFieldINT, jTextFieldPontosDisponiveis4);
                Status.setTotais(jTextFieldATKI, jTextFieldINT);
                return;
            }

            if (jRadioButtonSAB.isSelected()) {
                Ficha.upar(jTextFieldSAB, jTextFieldPontosDisponiveis4);
                Status.setTotais(jTextFieldATKS, jTextFieldSAB);
                return;
            }

            if (jRadioButtonSOR.isSelected()) {
                Ficha.upar(jTextFieldSOR, jTextFieldPontosDisponiveis4);
                return;
            }

            if (jRadioButtonRES.isSelected()) {
                Ficha.upar(jTextFieldRES, jTextFieldPontosDisponiveis4);
                Status.setTotais(jTextFieldDEF, jTextFieldRES);
                return;
            }

        } else {
            JOptionPane.showMessageDialog(null, "Não há skill points disponível!");
        }

    }//GEN-LAST:event_jButtonConfirmarUPActionPerformed

